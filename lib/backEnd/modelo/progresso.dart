class Progresso {
  late int id;
  late int quant_aula;
  late int aluno_id;

  int get getId {
    return id;
  }

  set setId(int id) {
    this.id = id;
  }

  int get getQuant_aula {
    return quant_aula;
  }

  set setQuant_aula(int quant_aula) {
    this.quant_aula = quant_aula;
  }

  int get getAlunoId {
    return aluno_id;
  }

  set setAlunoId(int aluno_id) {
    this.aluno_id = aluno_id;
  }
}
